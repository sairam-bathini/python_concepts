'''Create a list
append an element in the list
insert an element in the list
remove an element in the list
accessing element from list using indexing
'''
# Creating a list
movie_recommendations_list = ['sye','badra','chirutha','dhruva','eega']

# Add a new element at the end
movie_recommendations_list.append('f2')

# Inserting an element at a specific position
movie_recommendations_list.insert(1, 'agni')

# Removing a particular element
movie_recommendations_list.remove('eega')

# Accessing elements using indexing
print(movie_recommendations_list[1])
